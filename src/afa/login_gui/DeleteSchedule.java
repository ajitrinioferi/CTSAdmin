    private void DeletepanelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_DeletepanelMouseClicked
        try {
            // TODO add your handling code here:
            String query = "DELETE FROM schedule WHERE ScheduleID = ? AND SeatsUnvailable = 0;";
            PreparedStatement preparedStmt = AFAUtils.conn.prepareStatement(query);
            preparedStmt.setString(1, (String) ComboBox_ScheduleID.getSelectedItem());
            int affectedRow = preparedStmt.executeUpdate();
            if (affectedRow > 0) {
                JOptionPane.showMessageDialog(null,
                        "Schedule Deleted Successfuly!!",
                        "Success",
                        JOptionPane.INFORMATION_MESSAGE);
                this.dispose();
                DeleteSchedule x = new DeleteSchedule();
                x.setVisible(true);
                UpdateForm();
            } else {
                JOptionPane.showMessageDialog(null,
                        "Schedule failed to delete!!",
                        "Failed",
                        JOptionPane.WARNING_MESSAGE);
            }
        } catch (SQLException ex) {
            Logger.getLogger(DeleteSchedule.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_DeletepanelMouseClicked

